        <h1>History of Adoptapedia</h1>
        <p>On February 25th, 2013 when I was a senior in high school, I founded the <a target="_blank" href="http://adoptapedia.deviantart.com/">Adoptapedia group</a> on DeviantArt. At the time, the Adoptable community was booming; hundreds of new Adoptable groups were being founded, and artists were searching for more ways to spread their art across the site.</p>
		<p>The group started as an excel spreadsheet with a simple list of 30 groups. I spent hours formatting the outputs using text documents before pasting the BBCodes for DeviantArt groups into journal entries on the site. But the number quickly grew too big, and instead I transfered the list into a Java program. The script only returned a list of formatted of BBCode in alphabetical order, but I was proud of it at the time.</p>
		<p>This project followed me into college when I gravitated towards computer science. After completing the data structures course taught in C++, I rewrote the core program in C++. After I became an assistant TA for the introductory computer science course taught in Python - where my affinity for Python flourished - I rewrote the core program again in Python 2.7. <a target="_blank" href="https://github.com/Pepper-Wood/Adoptapedia">The outdated C++ and Python code can still be found under my GitHub in this repository</a>.</p>
		<p>In the Fall of 2016, I pitched this project for a senior-level software design and development course. With the help of my teammates Tyler, Kate, and Alex, Adoptapedia was adapted into a proper, self-updating database here at <a href="http://adoptapedia.com/">adoptapedia.com</a>. The project continues to be developed. I look forward to seeing what the future holds for this project.</p>
